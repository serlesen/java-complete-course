package com.sergio.javacourse.two.constructor;

/**
 * Create the constructor for this object with two fields:
 * - a string named "name";
 * - a float named "price".
 *
 */
public class Game {
	
	private String name;
	private float price;

	public Game(String name, float price) {
		this.name = name;
		this.price = price;
	}
}
