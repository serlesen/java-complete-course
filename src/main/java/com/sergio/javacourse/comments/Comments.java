package com.sergio.javacourse.comments;

public class Comments {
	
	public static void comment() {
		
	}
	
	/**
	 * The following method should return a value of 2.
	 * Use a single line comment to comment the lines where a different value from 2 is set into the variable 'i'.
	 */
	public static int one() {
		int i;
		i = 1;
		i = 2;
		i = 3;
		return i;
	}
	
	/**
	 * The following method should return a value of 5.
	 * Use a multiple line comment to comment the lines where a different value from 5 is set into the variable 'i'.
	 */
	public static int two() {
		int i;
		i = 1;
		i = 2;
		i = 3;
		i = 4;
		i = 5;
		i = 6;
		i = 7;
		i = 8;
		i = 9;
		return i;
	}

	/**
	 * The following method should return a value of 2.
	 * Use the same syntax as the previous method to comment only a part within a line where different values from
	 * 2 are set into the variable 'i'.
	 */
	public static int three() {
		int i;
		i = 1; i = 2; i = 3;
		return i;
	}

}
